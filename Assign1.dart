void main() {
  // Integers (int)
  int age = 25;
  print('Age: $age');

  // Floating-point numbers (double)
  double height = 5.9;
  print('Height: $height');

  // Strings (String)
  String name = 'John';
  print('Name: $name');

  // Lists
  List<String> fruits = ['Apple', 'Banana', 'Orange'];
  print('Fruits: $fruits');

  // Maps
  Map<String, dynamic> person = {
    'name': 'Alice',
    'age': 30,
    'isStudent': true,
  };
  print('Person: $person');
}
